package examples.tenta;

public class Question7 {
    public static void main(String[] args) {
        // Soru 7: Java'daki iki yorum operatörü (// ve /*) arasındaki fark nedir?
        // Açıklama:
        // - //: Tek satırlık yorumlar için kullanılır.
        // - /* */: Çok satırlı yorumlar için kullanılır.

        System.out.println("// Tek satırlık yorumlar için kullanılır.");
        System.out.println("/* */ Çok satırlı yorumlar için kullanılır.");
    }
}