package examples.tenta;


public class Question1 {
    public static void main(String[] args) {
        // Soru 1: java ve javac komutları ne için kullanılır?
        // Cevap:
        // - javac: Java kodunu (örneğin .java dosyalarını) bytecode'a (örneğin .class dosyalarına) derler.
        // - java: Bytecode'u (örneğin .class dosyalarını) Java Sanal Makinesi (JVM) üzerinde çalıştırır.

        System.out.println("javac: Compiles Java code (e.g., .java files) into bytecode (.class files).");
        System.out.println("java: Executes bytecode (e.g., .class files) in the Java Virtual Machine (JVM).");
    }
}