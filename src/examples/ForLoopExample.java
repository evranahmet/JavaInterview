package examples;

public class ForLoopExample {
    // 0'dan 14'e kadar olan sayıları yazdıran bir for döngüsü oluşturun. Döngü her adımda 1 artacak şekilde çalışmalıdır.
    public static void main(String[] args) {
        for (int i = 0; i < 15; i++) {
            System.out.println(i + ": This for-loop is now executing!");
        }
    }
}
